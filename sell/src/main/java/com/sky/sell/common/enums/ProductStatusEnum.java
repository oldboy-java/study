package com.sky.sell.common.enums;

import lombok.Getter;

/**
 * 商品状态
 * @author	刘力
 * @date	2017年12月2日下午2:39:31
 * @version 1.0
 */
@Getter
public enum ProductStatusEnum {

	UP(0,"上架"),DOWN(1,"下架");
	
	private Integer code;
	private String desc;
	
	private ProductStatusEnum(Integer code, String desc) {
		this.code = code;
		this.desc = desc;
	}
}
